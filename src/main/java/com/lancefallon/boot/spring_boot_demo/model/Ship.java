package com.lancefallon.boot.spring_boot_demo.model;

public class Ship {

	private String name;
	private Integer age;
	
	public Ship(){}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Integer getAge() {
		return age;
	}
	public void setAge(Integer age) {
		this.age = age;
	}
	
	
	
}
